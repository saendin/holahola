public class Comment {
/*
 * 람다식 문법
 * (타입 매개변수, ...) -> {실행문;}
 * 
 * 예) Interface value = (int a) -> {system.out.println(a);}
 * Interface value => (int a) -> {system.out.println(a);}를 담아주는 변수.
 *  (int a)라는 매개변수로 {system.out.println(a);}구현
 
 *  호출할 때 매소드 이름이 필요. 인터페이스 타입의 value로는 호출을 할 수 없음.
 *  
 * 단순히 실행 뿐 아니라 선언도 하는 것임.
 * 사용하고자 하는 변수를 미리 정의하는 것
 */
}
